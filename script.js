const FADE_MS = 220;

function fadeUpdate(elements, done) {
  const list = elements.filter(Boolean);
  if (!list.length) {
    done();
    return;
  }
  list.forEach((el) => {
    el.style.opacity = "0";
  });
  window.setTimeout(() => {
    done();
    window.requestAnimationFrame(() => {
      list.forEach((el) => {
        el.style.opacity = "1";
      });
    });
  }, FADE_MS);
}

function applyLanguage(lang, animate = true) {
  const dict = translations[lang];
  const isArabic = lang === "ar";
  html.lang = isArabic ? "ar" : "en";
  html.dir = isArabic ? "rtl" : "ltr";

  const flush = () => {
    i18nNodes.forEach((node) => {
      const key = node.dataset.i18n;
      if (dict[key]) {
        node.textContent = dict[key];
      }
    });
    if (langToggleLabel) {
      langToggleLabel.textContent = isArabic ? "English" : "\u0627\u0644\u0639\u0631\u0628\u064a\u0629";
    }
    localStorage.setItem("lang", lang);
  };

  if (animate && langToggleLabel) {
    fadeUpdate([langToggleLabel], flush);
  } else {
    flush();
  }
}

function applyTheme(theme, animate = true) {
  const isDark = theme === "dark";

  const flush = () => {
    body.classList.toggle("dark-mode", isDark);
    if (themeToggleLabel) {
      themeToggleLabel.textContent = isDark ? "Light" : "Dark";
    }
    if (themeIcon) {
      themeIcon.textContent = isDark ? "\u2600" : "\u263D";
    }
    localStorage.setItem("theme", theme);
  };

  if (animate && (themeToggleLabel || themeIcon)) {
    fadeUpdate([themeToggleLabel, themeIcon], flush);
  } else {
    flush();
  }
}

